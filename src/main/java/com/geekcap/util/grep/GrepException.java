package com.geekcap.util.grep;

public class GrepException extends Exception 
{

	public GrepException() 
	{
		super();
	}

	public GrepException( String message, Throwable cause ) 
	{
		super( message, cause );
	}

	public GrepException( String message ) 
	{
		super( message );
	}

	public GrepException( Throwable cause ) 
	{
		super( cause );
	}

}
